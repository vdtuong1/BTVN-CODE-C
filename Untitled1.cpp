#include <stdio.h>

int main() {
    // khai bao va khoi tao cac bien 

    int a = 10;          
    float b = 3.14f;     
    double c = 3.14159;  
    char d = 'A';        
    short e = 5;        
    long f = 100000L;   
    
   // in ra gtri cua bien 
    printf("a = %d\n", a);  
    printf("b = %f\n", b);  
    printf("c = %lf\n", c); 
    printf("d = %c\n", d);  
    printf("e = %d\n", e);  
    printf("f = %ld\n", f); 
    printf("g = %u\n", g);  

    return 0;
}

